    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/timetablesystem","root","");
            
          
            int row = jTableSubject.getSelectedRow();
            String value = (jTableSubject.getModel().getValueAt(row, 0).toString());
            String query = "UPDATE subject SET name=?, code=?, year=?, semester=?, lecHrs=?, tutHrs=?, labHrs=?, evlHrs=? where id="+value;
            PreparedStatement pst = conn.prepareStatement(query);
            
            
            pst.setString(1, txtSubName.getText());
            pst.setString(2, txtCode.getText());
            
            String Year;
            Year = txtYear.getSelectedItem().toString();
            pst.setString(3, Year);
          
            int sem;
            if(sem1.isSelected()){
                sem = 1;
                pst.setInt(4, sem);
            }
            else if(sem2.isSelected()){
                sem = 2;
                pst.setInt(4, sem);
            }
            
            pst.setInt(5, Integer.parseInt(txtLecHrs.getText()));
            pst.setInt(6, Integer.parseInt(txtTutHrs.getText()));
            pst.setInt(7, Integer.parseInt(txtLabHrs.getText()));
            pst.setInt(8, Integer.parseInt(txtEvlHrs.getText()));
            //pst.setString(7, txtRank.getText());
            
            pst.executeUpdate();
            
            //refresh table after insert data
            DefaultTableModel model = (DefaultTableModel)jTableSubject.getModel();
            model.setRowCount(0);
            displaytable();
            
            
            JOptionPane.showMessageDialog(this, "subject updated");
            
            conn.close();
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
            
        
    }//GEN-LAST:event_btnUpdateActionPerformed

