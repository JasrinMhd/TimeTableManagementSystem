    private void jTableSubjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSubjectMouseClicked
        // TODO add your handling code here:
        int i = jTableSubject.getSelectedRow();
        TableModel model = jTableSubject.getModel();
        //id, name, code, year, semester, lecHrs, tutHrs, labHrs, evlHrs
        
        txtSubName.setText(model.getValueAt(i,1).toString());
        txtCode.setText(model.getValueAt(i,2).toString());
        
        String Fac = model.getValueAt(i,3).toString();
            switch(Fac){ //
                case "1":
                    txtYear.setSelectedIndex(0);
                    break;
                case "2":
                    txtYear.setSelectedIndex(1);
                    break;
                case "3":
                    txtYear.setSelectedIndex(2);
                    break;
                case "4":
                    txtYear.setSelectedIndex(3);
                    break;
               
            }
      String sem = model.getValueAt(i,4).toString();
      if(sem.equals("1")){
          sem1.setSelected(true);
      }
      else if(sem.equals("2")){
          sem2.setSelected(true);
      }
     txtLecHrs.setText(model.getValueAt(i,7).toString());
     txtTutHrs.setText(model.getValueAt(i,7).toString());
     txtLabHrs.setText(model.getValueAt(i,7).toString());
     txtEvlHrs.setText(model.getValueAt(i,7).toString());
        
        
    }//GEN-LAST:event_jTableSubjectMouseClicked

