    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        
        // disable editing student ID
        StudentIDField.setEditable(false);
        
        //fetching student data from jtable to form
        
        DefaultTableModel model = (DefaultTableModel)studentTable.getModel();
        int selectedRowIndex = studentTable.getSelectedRow();
        
        StudentIDField.setText(model.getValueAt(selectedRowIndex, 0).toString()); // Student Group ID
        
        String AYSCombo = model.getValueAt(selectedRowIndex, 1).toString();     //Academic Year and Semester
        for(int i = 0; i < EditAYSfield.getItemCount(); i ++){
        if (EditAYSfield.getItemAt(i).toString().equalsIgnoreCase(AYSCombo)){
            EditAYSfield.setSelectedIndex(i);
            }
        }
        
        String ProgrammeCombo = model.getValueAt(selectedRowIndex, 2).toString(); //Programme
        for(int i = 0; i < EditProgrammeField.getItemCount(); i ++){
        if (EditProgrammeField.getItemAt(i).toString().equalsIgnoreCase(ProgrammeCombo)){
            EditProgrammeField.setSelectedIndex(i);
            }
        }
        
        
        EditGroupNoField.setText(model.getValueAt(selectedRowIndex, 3).toString());     //Group Number
        EditSubGroupNoField.setText(model.getValueAt(selectedRowIndex, 4).toString());  //SubGroup Number
        EditGroupIDField.setText(model.getValueAt(selectedRowIndex, 5).toString());     //GroupID
        EditSubGroupIDField.setText(model.getValueAt(selectedRowIndex, 6).toString());  //SubGroupID
    }//GEN-LAST:event_studentTableMouseClicked

