    private void AddStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddStudentBtnActionPerformed
       
        // Validation...
        if(AYSfield.getSelectedItem().toString().isEmpty() ||  Programmefield.getSelectedItem().toString().isEmpty() ||  GroupNoField.getText().trim().isEmpty() ||  SubGroupNoField.getText().trim().isEmpty() || GroupIDfield.getText().trim().isEmpty() ||  SubGroupIDfield.getText().trim().isEmpty()){
            EmptyMsg3Lbl.setText("Please fill all the details ");
            GroupIDfield.setText("");
            SubGroupIDfield.setText("");
        }
        else{
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/timetablesystem","root","");

                //adding student data to database
                String sql = "insert into student(AcademicYearSemester,Programme,GroupNo,SubGroupNo,GroupID,SubGroupID) values (?,?,?,?,?,?)";
                PreparedStatement pstmt = conn.prepareStatement(sql);

                pstmt.setString(1, AYSfield.getSelectedItem().toString());
                pstmt.setString(2, Programmefield.getSelectedItem().toString());
                pstmt.setInt(3, Integer.parseInt(GroupNoField.getText()));
                pstmt.setInt(4, Integer.parseInt(SubGroupNoField.getText()));
                pstmt.setString(5, GroupIDfield.getText());
                pstmt.setString(6, SubGroupIDfield.getText());

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Student details added Successfully");
                conn.close();

                // clear data after adding to database
                AYSfield.setSelectedItem("");
                Programmefield.setSelectedItem("");
                GroupNoField.setText("");
                SubGroupNoField.setText("");
                GroupIDfield.setText("");
                SubGroupIDfield.setText("");

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/timetablesystem","root","");
            
            //Selecting updated data from database to Student Table after adding
            PreparedStatement pstmt = conn.prepareStatement("select * from student");
            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel tm=(DefaultTableModel)studentTable.getModel();
            tm.setRowCount(0);

            while (rs.next()){
                Object o[] = {rs.getInt("StudentsID"),rs.getString("AcademicYearSemester"),rs.getString("Programme"),rs.getString("GroupNo"),rs.getString("SubGroupNo"),rs.getString("GroupID"),rs.getString("SubGroupID")};
                tm.addRow(o);
            }

        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_AddStudentBtnActionPerformed

